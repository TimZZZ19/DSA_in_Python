# Python Data Types Demonstration


def main():
    # 1. Integer
    age = 25
    print(f"Integer (age): {age}, Type: {type(age)}")

    # 2. Float
    height = 5.9
    print(f"Float (height): {height}, Type: {type(height)}")

    # 3. String
    name = "Alice"
    print(f"String (name): {name}, Type: {type(name)}")

    # 4. Boolean
    is_active = True
    print(f"Boolean (is_active): {is_active}, Type: {type(is_active)}")

    # 5. List
    fruits = ["apple", "banana", "cherry"]
    print(f"List (fruits): {fruits}, Type: {type(fruits)}")


if __name__ == "__main__":
    main()
